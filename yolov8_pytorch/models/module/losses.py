class VarifocalLoss(nn.Module):
    """VarifocalLoss is a loss function used to train dense target detectors.

    It is an extension of Focal Loss and the loss can be adjusted according to the target's IoU value.
    Focal Loss is a loss function used to solve the problem of imbalance between positive and negative samples.
    It solves this problem by increasing the loss of negative samples.
    VarifocalLoss adjusts the loss of positive and negative samples by introducing a new parameter α.
    The smaller the value of α is, the smaller the loss to the positive sample is and the greater the loss to the negative sample is.

    Reference:
        https://arxiv.org/abs/2008.13367.
    """

    def __init__(self):
        super().__init__()

    @staticmethod
    def forward(predicted_scores: Tensor, ground_truth_scores: Tensor, labels: Tensor, alpha: float = 0.75, gamma: float = 2.0) -> Tensor:
        r"""Computes varifocal loss.

        Args:
            predicted_scores (Tensor): The predicted scores from the model.
            ground_truth_scores (Tensor): The ground truth scores.
            labels (Tensor): The ground truth labels.
            alpha (float, optional): The alpha parameter for varifocal loss calculation. Defaults to 0.75.
            gamma (float, optional): The gamma parameter for varifocal loss calculation. Defaults to 2.0.

        Returns:
            Tensor: The computed varifocal loss.

        Examples:
            >>> predicted_scores = torch.tensor([0.1, 0.2, 0.3, 0.4])
            >>> ground_truth_scores = torch.tensor([0.2, 0.3, 0.4, 0.5])
            >>> labels = torch.tensor([0, 1, 0, 1])
            >>> varifocal_loss = VarifocalLoss()
            >>> varifocal_loss(predicted_scores, ground_truth_scores, labels)
            tensor(0.3408)
        """
        # Calculate the weight for varifocal loss
        varifocal_weight = alpha * predicted_scores.sigmoid().pow(gamma) * (1 - labels) + ground_truth_scores * labels

        # Compute the varifocal loss
        with torch.cuda.amp.autocast(enabled=False):
            varifocal_loss = (F_torch.binary_cross_entropy_with_logits(predicted_scores.float(),
                                                                       ground_truth_scores.float(), reduction="none") * varifocal_weight).mean(
                1).sum()
        return varifocal_loss


class FocalLoss(nn.Module):
    """FocalLoss is a loss function used in object detection tasks to address the problem of class imbalance.
    It modulates the loss for each sample in a way that the loss for well-classified examples is down-weighted.

    Reference:
        https://arxiv.org/abs/1708.02002
    """

    def __init__(self):
        super().__init__()

    @staticmethod
    def forward(predicted_labels: torch.Tensor, true_labels: torch.Tensor, gamma: float = 1.5, alpha: float = 0.25) -> torch.Tensor:
        r"""Compute the focal loss.

        Args:
            predicted_labels (torch.Tensor): The predicted labels from the model.
            true_labels (torch.Tensor): The ground truth labels.
            gamma (float, optional): The gamma parameter for focal loss calculation. Defaults to 1.5.
            alpha (float, optional): The alpha parameter for focal loss calculation. Defaults to 0.25.

        Returns:
            torch.Tensor: The computed focal loss.

        Examples:
            >>> predicted_labels = torch.tensor([0.1, 0.2, 0.3, 0.4])
            >>> true_labels = torch.tensor([0.2, 0.3, 0.4, 0.5])
            >>> focal_loss = FocalLoss()
            >>> focal_loss(predicted_labels, true_labels)
            tensor(0.0009)
        """
        # Compute the binary cross entropy loss
        binary_cross_entropy_loss = F_torch.binary_cross_entropy_with_logits(predicted_labels, true_labels, reduction='none')

        # Compute the predicted probabilities from logits
        predicted_probabilities = predicted_labels.sigmoid()

        # Compute the modulating factor
        p_t = true_labels * predicted_probabilities + (1 - true_labels) * (1 - predicted_probabilities)
        modulating_factor = (1.0 - p_t) ** gamma

        # Apply the modulating factor to the loss
        loss = binary_cross_entropy_loss * modulating_factor

        # If alpha is greater than 0, compute the alpha factor and apply it to the loss
        if alpha > 0:
            alpha_factor = true_labels * alpha + (1 - true_labels) * (1 - alpha)
            loss *= alpha_factor

        # Return the mean loss over all samples
        return loss.mean(1).sum()


class BboxLoss(nn.Module):
    """Criterion class for computing training losses during training."""

    def __init__(self, reg_max, use_dfl=False):
        """Initialize the BboxLoss module with regularization maximum and DFL settings."""
        super().__init__()
        self.reg_max = reg_max
        self.use_dfl = use_dfl

    def forward(self, pred_dist, pred_bboxes, anchor_points, target_bboxes, target_scores, target_scores_sum, fg_mask):
        """IoU loss."""
        weight = target_scores.sum(-1)[fg_mask].unsqueeze(-1)
        iou = bbox_iou(pred_bboxes[fg_mask], target_bboxes[fg_mask], xywh=False, CIoU=True)
        loss_iou = ((1.0 - iou) * weight).sum() / target_scores_sum

        # DFL loss
        if self.use_dfl:
            target_ltrb = convert_bbox_to_letterbox(anchor_points, target_bboxes, self.reg_max)
            loss_dfl = self._df_loss(pred_dist[fg_mask].view(-1, self.reg_max + 1), target_ltrb[fg_mask]) * weight
            loss_dfl = loss_dfl.sum() / target_scores_sum
        else:
            loss_dfl = torch.tensor(0.0).to(pred_dist.device)

        return loss_iou, loss_dfl

    @staticmethod
    def _df_loss(pred_dist, target):
        """Return sum of left and right DFL losses."""
        # Distribution Focal Loss (DFL) proposed in Generalized Focal Loss https://ieeexplore.ieee.org/document/9792391
        tl = target.long()  # target left
        tr = tl + 1  # target right
        wl = tr - target  # weight left
        wr = 1 - wl  # weight right
        return (F_torch.cross_entropy(pred_dist, tl.view(-1), reduction='none').view(tl.shape) * wl +
                F_torch.cross_entropy(pred_dist, tr.view(-1), reduction='none').view(tl.shape) * wr).mean(-1, keepdim=True)


class v8DetectionLoss:
    """Criterion class for computing training losses."""

    def __init__(self, model):  # model must be de-paralleled
        """Initializes v8DetectionLoss with the model, defining model-related properties and BCE loss function."""
        device = next(model.parameters()).device  # get model device
        h = model.args  # hyperparameters

        m = model.model[-1]  # Detect() module
        self.bce = nn.BCEWithLogitsLoss(reduction='none')
        self.hyp = h
        self.stride = m.stride  # model strides
        self.nc = m.nc  # number of classes
        self.no = m.no
        self.reg_max = m.reg_max
        self.device = device

        self.use_dfl = m.reg_max > 1

        self.assigner = TaskAlignedAssigner(topk=10, num_classes=self.nc, alpha=0.5, beta=6.0)
        self.bbox_loss = BboxLoss(m.reg_max - 1, use_dfl=self.use_dfl).to(device)
        self.proj = torch.arange(m.reg_max, dtype=torch.float, device=device)

    def preprocess(self, targets, batch_size, scale_tensor):
        """Preprocesses the target counts and matches with the input batch size to output a tensor."""
        if targets.shape[0] == 0:
            out = torch.zeros(batch_size, 0, 5, device=self.device)
        else:
            i = targets[:, 0]  # image index
            _, counts = i.unique(return_counts=True)
            counts = counts.to(dtype=torch.int32)
            out = torch.zeros(batch_size, counts.max(), 5, device=self.device)
            for j in range(batch_size):
                matches = i == j
                n = matches.sum()
                if n:
                    out[j, :n] = targets[matches, 1:]
            out[..., 1:5] = xywh2xyxy(out[..., 1:5].mul_(scale_tensor))
        return out

    def bbox_decode(self, anchor_points, pred_dist):
        """Decode predicted object bounding box coordinates from anchor points and distribution."""
        if self.use_dfl:
            b, a, c = pred_dist.shape  # batch, anchors, channels
            pred_dist = pred_dist.view(b, a, 4, c // 4).softmax(3).matmul(self.proj.type(pred_dist.dtype))
            # pred_dist = pred_dist.view(b, a, c // 4, 4).transpose(2,3).softmax(3).matmul(self.proj.type(pred_dist.dtype))
            # pred_dist = (pred_dist.view(b, a, c // 4, 4).softmax(2) * self.proj.type(pred_dist.dtype).view(1, 1, -1, 1)).sum(2)
        return convert_letterbox_to_bbox(pred_dist, anchor_points, xywh=False)

    def __call__(self, preds, batch):
        """Calculate the sum of the loss for box, cls and dfl multiplied by batch size."""
        loss = torch.zeros(3, device=self.device)  # box, cls, dfl
        feats = preds[1] if isinstance(preds, tuple) else preds
        pred_distri, pred_scores = torch.cat([xi.view(feats[0].shape[0], self.no, -1) for xi in feats], 2).split(
            (self.reg_max * 4, self.nc), 1)

        pred_scores = pred_scores.permute(0, 2, 1).contiguous()
        pred_distri = pred_distri.permute(0, 2, 1).contiguous()

        dtype = pred_scores.dtype
        batch_size = pred_scores.shape[0]
        imgsz = torch.tensor(feats[0].shape[2:], device=self.device, dtype=dtype) * self.stride[0]  # image size (h,w)
        anchor_points, stride_tensor = make_anchors(feats, self.stride, 0.5)

        # Targets
        targets = torch.cat((batch['batch_idx'].view(-1, 1), batch['cls'].view(-1, 1), batch['bboxes']), 1)
        targets = self.preprocess(targets.to(self.device), batch_size, scale_tensor=imgsz[[1, 0, 1, 0]])
        gt_labels, gt_bboxes = targets.split((1, 4), 2)  # cls, xyxy
        mask_gt = gt_bboxes.sum(2, keepdim=True).gt_(0)

        # Pboxes
        pred_bboxes = self.bbox_decode(anchor_points, pred_distri)  # xyxy, (b, h*w, 4)

        _, target_bboxes, target_scores, fg_mask, _ = self.assigner(
            pred_scores.detach().sigmoid(), (pred_bboxes.detach() * stride_tensor).type(gt_bboxes.dtype),
            anchor_points * stride_tensor, gt_labels, gt_bboxes, mask_gt)

        target_scores_sum = max(target_scores.sum(), 1)

        # Cls loss
        # loss[1] = self.varifocal_loss(pred_scores, target_scores, target_labels) / target_scores_sum  # VF_torchL way
        loss[1] = self.bce(pred_scores, target_scores.to(dtype)).sum() / target_scores_sum  # BCE

        # Bbox loss
        if fg_mask.sum():
            target_bboxes /= stride_tensor
            loss[0], loss[2] = self.bbox_loss(pred_distri, pred_bboxes, anchor_points, target_bboxes, target_scores,
                                              target_scores_sum, fg_mask)

        loss[0] *= self.hyp.box  # box gain
        loss[1] *= self.hyp.cls  # cls gain
        loss[2] *= self.hyp.dfl  # dfl gain

        return loss.sum() * batch_size, loss.detach()  # loss(box, cls, dfl)


